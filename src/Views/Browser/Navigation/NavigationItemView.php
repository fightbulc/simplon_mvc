<?php

namespace Simplon\Mvc\Views\Browser\Navigation;

/**
 * Class NavigationItemView
 * @package Simplon\Mvc\Views\Browser\Navigation
 */
class NavigationItemView
{
    /**
     * @var string
     */
    private $id;

    /**
     * @var string
     */
    private $label;

    /**
     * @var string
     */
    private $url;

    /**
     * @return string
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param string $id
     *
     * @return NavigationItemView
     */
    public function setId($id)
    {
        $this->id = $id;

        return $this;
    }

    /**
     * @return string
     */
    public function getLabel()
    {
        return $this->label;
    }

    /**
     * @param string $label
     *
     * @return NavigationItemView
     */
    public function setLabel($label)
    {
        $this->label = $label;

        return $this;
    }

    /**
     * @return string
     */
    public function getUrl()
    {
        return $this->url;
    }

    /**
     * @param string $url
     *
     * @return NavigationItemView
     */
    public function setUrl($url)
    {
        $this->url = $url;

        return $this;
    }
}